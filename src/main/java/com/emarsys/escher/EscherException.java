package com.emarsys.escher;

public class EscherException extends java.lang.Exception {

    public EscherException(String message) {
        super(message);
    }


    public EscherException(String message, Throwable cause) {
        super(message, cause);
    }


    public EscherException(Throwable cause) {
        super(cause);
    }


    public EscherException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
